    # ── 1️⃣ Through hole (Ø4) ──
    hole = Solid.make_cylinder(2.0, z_top - z_bot)
    hole = hole.moved(Location(Vector(hx, hy, z_bot)))
    result = result.cut(hole)

    # ── 2️⃣ Countersink (Ø8 at top) ──
    cs_height = 2.0   # ✔ best for ~90° countersink

    # IMPORTANT: correct orientation (top wide, bottom narrow)
    cone = Solid.make_cone(4.0, 2.0, cs_height)

    # place exactly at top
    cone = cone.moved(Location(Vector(hx, hy, z_top - cs_height)))

    result = result.cut(cone)

    try:
        result = result.solids()[0]
    except:
        pass

    print(f"✅ Hole + CSK at ({hx:.2f}, {hy:.2f})")


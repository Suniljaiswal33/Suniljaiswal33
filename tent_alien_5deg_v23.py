# ── Helpers ──
def centroid(pts):
    return sum(p[0] for p in pts)/len(pts), sum(p[1] for p in pts)/len(pts)

def shrink_pts(pts, z_h, deg):
    cx, cy = centroid(pts)
    shrink = math.tan(math.radians(deg)) * z_h
    out = []
    for x, y in pts:
        dx, dy = x-cx, y-cy
        dist   = math.hypot(dx, dy)
        if dist < 1e-6:
            out.append((x, y)); continue
        nd = max(dist - shrink, 0)
        out.append((cx + dx/dist*nd, cy + dy/dist*nd))
    return out

def make_boss(pts, height, taper_deg):
    bot   = [Vector(x, y,  0)       for x, y in pts]
    top   = [Vector(x, y, -height)  for x, y in shrink_pts(pts, height, taper_deg)]
    w_bot = Wire.make_polygon(bot, close=True)
    w_top = Wire.make_polygon(top, close=True)
    return Solid.make_loft([w_bot, w_top])

def make_countersink(cx, cy, thru_r, csink_r, csink_depth, total_h):
    # Z=40 (top): dia80 → Z=20: dia40 → Z=0: straight thru
    cone = Solid.make_cone(csink_r, thru_r, csink_depth,
                           pnt=Vector(cx, cy, total_h),
                           dir=Vector(0, 0, -1))
    cyl  = Solid.make_cylinder(thru_r, total_h - csink_depth,
                               pnt=Vector(cx, cy, 0),
                               dir=Vector(0, 0, 1))
    r = cone.fuse(cyl)
    try:    return r.solids()[0]
    except: return r

def safe_fuse(base, tool):
    r = base.fuse(tool)
    try:    return r.solids()[0]
    except: return r

def safe_cut(base, tool):
    r = base.cut(tool)
    try:    return r.solids()[0]
    except: return r

